// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, output = []) {
  
    if (output.length >= 1 && start === end){
      output.push(start)
      return output
      
    } else if (start === end) {
      return output
    }
    if (start < end) {
      output.push(start)
      return range(start + 1, end, output)
    }
    if (start > end) {
      output.unshift(start)
      return range(start - 1, end, output)
    }
  
}

